const API_BASE = "https://api.github.com";

function requireEnv(name) {
  const value = process.env[name];
  if (!value) {
    throw new Error(`Missing required environment variable: ${name}`);
  }
  return value;
}

function toIsoDate(input) {
  return input.toISOString().slice(0, 10);
}

function isPullRequest(issue) {
  return Boolean(issue.pull_request);
}

function isBlocked(item) {
  const labels = item.labels ?? [];
  return labels.some((label) => {
    const name = typeof label === "string" ? label : (label.name ?? "");
    return name.toLowerCase().includes("blocked");
  });
}

async function request(path, options = {}) {
  const token = requireEnv("GITHUB_TOKEN");
  const response = await fetch(`${API_BASE}${path}`, {
    ...options,
    headers: {
      Accept: "application/vnd.github+json",
      Authorization: `Bearer ${token}`,
      "X-GitHub-Api-Version": "2022-11-28",
      ...(options.headers ?? {}),
    },
  });

  if (!response.ok) {
    const errorText = await response.text();
    throw new Error(`GitHub API ${response.status} ${path}: ${errorText}`);
  }

  return response.json();
}

async function getIssues(owner, repo, state) {
  return request(`/repos/${owner}/${repo}/issues?state=${state}&per_page=100`);
}

async function getPulls(owner, repo, state) {
  return request(`/repos/${owner}/${repo}/pulls?state=${state}&per_page=100`);
}

function buildBody({
  projectBoardUrl,
  snapshotDate,
  openIssues,
  openPulls,
  mergedLast24h,
  closedIssuesLast24h,
  blockedIssues,
  blockedPulls,
}) {
  const header = `# Daily Progress Snapshot (${snapshotDate})`;
  const boardLine = projectBoardUrl
    ? `- GitHub Project Board: ${projectBoardUrl}`
    : "- GitHub Project Board: _set `PROJECT_BOARD_URL` variable to show link_";

  return [
    header,
    "",
    "## At a Glance",
    "",
    boardLine,
    `- Open issues: ${openIssues.length}`,
    `- Open pull requests: ${openPulls.length}`,
    `- Merged pull requests (last 24h): ${mergedLast24h.length}`,
    `- Closed issues (last 24h): ${closedIssuesLast24h.length}`,
    `- Current blockers: ${blockedIssues.length + blockedPulls.length}`,
    "",
    "## Blockers",
    "",
    ...(blockedIssues.length + blockedPulls.length === 0
      ? ["- None currently labeled as blocked."]
      : [
          ...blockedIssues.map(
            (issue) => `- Issue #${issue.number}: ${issue.title}`,
          ),
          ...blockedPulls.map((pr) => `- PR #${pr.number}: ${pr.title}`),
        ]),
    "",
    "## Completed in Last 24h",
    "",
    ...(mergedLast24h.length + closedIssuesLast24h.length === 0
      ? ["- No completions in the last 24 hours."]
      : [
          ...mergedLast24h.map(
            (pr) => `- Merged PR #${pr.number}: ${pr.title}`,
          ),
          ...closedIssuesLast24h.map(
            (issue) => `- Closed issue #${issue.number}: ${issue.title}`,
          ),
        ]),
    "",
    "## Notes",
    "",
    "- This issue is auto-generated by `.github/workflows/daily-progress-snapshot.yml`.",
    "- Use this as the daily checkpoint record and add it to the GitHub Project board.",
    "",
  ].join("\n");
}

async function upsertSnapshotIssue(owner, repo, title, body) {
  const allIssues = await getIssues(owner, repo, "all");
  const existing = allIssues.find((issue) => issue.title === title);

  if (existing) {
    await request(`/repos/${owner}/${repo}/issues/${existing.number}`, {
      method: "PATCH",
      body: JSON.stringify({ body }),
    });
    return { number: existing.number, action: "updated" };
  }

  const created = await request(`/repos/${owner}/${repo}/issues`, {
    method: "POST",
    body: JSON.stringify({
      title,
      body,
    }),
  });

  return { number: created.number, action: "created" };
}

async function main() {
  const repository = requireEnv("GITHUB_REPOSITORY");
  const [owner, repo] = repository.split("/");
  const projectBoardUrl = process.env.PROJECT_BOARD_URL ?? "";

  const now = new Date();
  const last24Hours = new Date(now.getTime() - 24 * 60 * 60 * 1000);
  const snapshotDate = toIsoDate(now);
  const title = `Daily Progress Snapshot (${snapshotDate})`;

  const [openIssuesRaw, openPulls, closedIssuesRaw, closedPulls] =
    await Promise.all([
      getIssues(owner, repo, "open"),
      getPulls(owner, repo, "open"),
      getIssues(owner, repo, "closed"),
      getPulls(owner, repo, "closed"),
    ]);

  const openIssues = openIssuesRaw.filter((item) => !isPullRequest(item));
  const closedIssues = closedIssuesRaw.filter((item) => !isPullRequest(item));

  const closedIssuesLast24h = closedIssues.filter(
    (issue) => issue.closed_at && new Date(issue.closed_at) >= last24Hours,
  );
  const mergedLast24h = closedPulls.filter(
    (pr) => pr.merged_at && new Date(pr.merged_at) >= last24Hours,
  );

  const blockedIssues = openIssues.filter(isBlocked);
  const blockedPulls = openPulls.filter(isBlocked);

  const body = buildBody({
    projectBoardUrl,
    snapshotDate,
    openIssues,
    openPulls,
    mergedLast24h,
    closedIssuesLast24h,
    blockedIssues,
    blockedPulls,
  });

  const result = await upsertSnapshotIssue(owner, repo, title, body);
  console.log(`Snapshot issue ${result.action}: #${result.number}`);
}

main().catch((error) => {
  console.error(error.message);
  process.exit(1);
});
